    public SinhVien getFormData(){
        String masv = txtMaSinhVien.getText();
        String tensv = txtTen.getText();
        int diem = Integer.parseInt(txtDiem.getText());
        SinhVien sv = new SinhVien(masv, tensv, diem);
        return sv;
    }
    
    public void load(ArrayList<SinhVien> list){
        DefaultTableModel tblModel = (DefaultTableModel) tblSinhVien.getModel();
        tblModel.setRowCount(0);
        for (SinhVien x : list) {
            Object[] thongTinSV = {x.getMaSV(),x.getTenSV(),x.getDiem()};
            tblModel.addRow(thongTinSV);
        }
        
    }
