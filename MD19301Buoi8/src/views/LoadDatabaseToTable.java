    private void tblSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSinhVienMouseClicked

        int index = tblSinhVien.getSelectedRow();
        txtMaSinhVien.setText((String)tblSinhVien.getValueAt(index, 0));
        txtTen.setText((String)tblSinhVien.getValueAt(index, 1));
        int diem = (Integer)tblSinhVien.getValueAt(index, 2);
        txtDiem.setText(String.valueOf(diem));        
    }//GEN-LAST:event_tblSinhVienMouseClicked

