    public Boolean checkPrimaryKey(){
        for(int i = 0; i < tblNhanVien.getRowCount();i++){
            String checkMa = (String)tblNhanVien.getValueAt(i, 0);
            if(checkMa.equalsIgnoreCase(txtMaNhanVien.getText())){
                JOptionPane.showMessageDialog(this, "Trung Khoa Chinh Vui Long nhap ma khac");
                return false;
            }     
        }
        return true;
    }
    
    public void initComboBox(){
        String[] chucVu = {"Trưởng Phòng","Nhân Viên"};
        DefaultComboBoxModel cboModel = new DefaultComboBoxModel(chucVu);
        cboChucVu.setModel(cboModel);
    }
    
    public Boolean checkValue(){
        if(txtMaNhanVien.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Chua nhap Ma Nhan Vien !!");
            txtMaNhanVien.requestFocus();
            return false;
        }
        else if(txtTen.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Chua nhap ten !!");
            txtTen.requestFocus();
            return false;
        }
        else return true;
    }
    
    public NhanVien getForm(){
            String ma = txtMaNhanVien.getText();
            String ten = txtTen.getText();
            boolean gioiTinh;
            if(rdoNam.isSelected()){
                gioiTinh = true;
            }
            else{
                gioiTinh = false;
            }
            String chucVu = (String)cboChucVu.getSelectedItem();
            NhanVien nv = new NhanVien(ma, ten, gioiTinh, chucVu);
            return nv;
    }
    
    public void loadTable(){
        ArrayList<NhanVien> nv = nvRepo.seacher();
        DefaultTableModel tblModel = (DefaultTableModel) tblNhanVien.getModel();
        tblModel.setRowCount(0);
        for (NhanVien x : nv) {
            Object[] thongTinNV = {x.getMaNhanVien(),x.getTen(),x.isGioiTinh()? "Nam":"Nữ",x.getChucVu()};
            tblModel.addRow(thongTinNV);
        }
    }

