    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if(txtMaNhanVien.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Chua nhap ma nen khong the xoa !!");
        }
        else{
            nvRepo.delete(txtMaNhanVien.getText());
            loadTable();
        }      
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        // TODO add your handling code here:
        int index = tblNhanVien.getSelectedRow();
        txtMaNhanVien.setText((String)tblNhanVien.getValueAt(index, 0));
        txtTen.setText((String)tblNhanVien.getValueAt(index, 1));
        String gioiTinh =(String)tblNhanVien.getValueAt(index, 2);
        if(gioiTinh == "Nam"){
            rdoNam.setSelected(true);
        }
        else{
            rdoNu.setSelected(true);
        }
        String chucVu = (String)tblNhanVien.getValueAt(index, 3);
        if(chucVu.equalsIgnoreCase(cboChucVu.getItemAt(0))){
            cboChucVu.setSelectedIndex(0);
        }else{
            cboChucVu.setSelectedIndex(1);
        }
    }//GEN-LAST:event_tblNhanVienMouseClicked

